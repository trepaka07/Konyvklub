        <h1> Könyvklub webáruház</h1>
        <h1> Felhasználói dokumentáció</h1>

        <h2> 1. Bevezetés</h2>
        <p> Jelen felhasználói kézikönyv a Könyvklub weboldal felhasználói számára készült.</p>
        <p> Ebben a fejezetben mutatjuk be az alkalmazás oldalainak felépítését és működését, egy-egy mintaképernyő segítségével.</p>

        <h2> 2. Rendszerkövetelmények</h2>
        <h3> Hardver követelmények</h3>
        <p> Az alkalmazás interneten keresztül elérhető webalapú szoftver, ezért használatához elegendő egy internetes böngésző program. A weboldal használatához folyamatos internetkapcsolat szükséges, viszont a megfelelő felhasználói élmény és gyorsaság miatt javasoljuk a megfelelő IT eszközökön történő használatát.</p>
        <h4> Minimum hardver követelmények:</h4>
        <ul>
            <li> Processzor: Intel Pentium D vagy jobb processzor</li>
            <li> RAM (memória): minimum 256 MB</li>
            <li> Merevlemez: legalább 200 MB szabad hely</li>
            <li> Internetkapcsolat: szükséges</li>
        </ul>
        <h4> Ajánlott hardver követelmények:</h4>
        <ul>
            <li> Processzor: Intel Core i3 4005U</li>
            <li> RAM (memória): 1 GB</li>
            <li> Merevlemez: legalább 50 GB szabad hely</li>
            <li> Internetkapcsolat: szükséges</li>
        </ul>
        <h3> Szoftver követelmények</h3>
        <h4> A weboldal futtatásához szükséges szoftverek:</h4>
        <ul>
            <li> Operációs rendszer: Windows 7 vagy újabb, Linux Ubuntu 14, Mac OS 10.13.6 High Sierra, Android 4.4, IOS 9 vagy újabb</li>
            <li> Böngésző: Google Chrome, Mozilla Firefox, Opera, Microsoft Edge</li>
        </ul>

        <h2> 3. A program telepítése</h2>
        <p> Mivel a weboldalt elérhető publikus domain szerverről, ezért a projekt használatához telepítésre nincs szükség.</p>

        <h2> 4. Kezdőoldal</h2>

        <p> A nyitóoldal felépítése, működése, a felhasználói felület illeszkedik a képernyő felbontásához.</p>
        <p> A weboldal betöltésekor a Főoldal nyílik meg, ha elkalandozna az oldalak között, és szeretne visszajutni a kezdő oldalra, akkor kattintson a bal felső sarokban lévő „Könyvklub” logóra. </p>
        <p> A kezdőoldal felső és alsó navigációs sávja minden oldalon elérhető, kiemelt színnel jelöltük.
            A felső navigációs sáv alatt láthatóak az első tíz „Legnépszerűbb könyveink”, a nyilak segítségével léptethetők jobb és bal irányban. Nyitóoldalunk szinte égészét könyvválasztékunk alkotja. A „Kosárhoz ad” gombra kattintva tehetjük könyvünket a kosárba. Könyvekre kattintva, új oldalon nyílik meg az adott könyv részletes leírása.</p>

        <img class="screen2" src="../../img/docs/nyitolap.png" alt="KÉP2">

        <h2> 5. Bejelentkezés/Kijelentkezés</h2>
        <p> A weboldal jobb felső sarkában található „Bejelentkezés” ikonra kattintva megjelenő előugró ablakban léphet be a felhasználó az oldalra, a regisztrációkor megadott e-mail címének és jelszavának megadásával. Amennyiben kipipálja az „Emlékezz rám” jelölőnégyzetet, a következő bejelentkezésnél már nem kell megadnia a jelszavát. </p>
        <p> Az „Elfelejtette jelszavát?” szövegre kattintva elindítja az új jelszóigénylés folyamatát. </p>
        <p> Az „Regisztráció” szövegre kattintva hozhatja létre felhasználói profilját.</p>

        <img class="screen1" src="../../img/docs/header_checkout.png" alt="KÉP1">
        <img class="screen2" src="../../img/docs/login.png" alt="KÉP2">

        <p> A profilból való kijelentkezést a jobb felső sarokban lévő „Kijelentkezés” ikonra vagy előtte lévő keresztnevére kattintva teheti meg. </p>

        <img class="screen1" src="../../img/docs/header_login.png" alt="KÉP1">

        <h2> 6. Regisztráció</h2>
        <p> Ha még nem rendelkezik saját profillal, kattintson a Regisztráció gombra, ekkor egy új oldal nyílik meg, ahol lehetősége van regisztrálni, személyes adatainak megadásával. </p>

        <img class="screen2" src="../../img/docs/signup.png" alt="KÉP2">

        <p> A „Regisztrálok” gomb megnyomása előtt el kell fogadnia az Általános szerződési feltételeket. Ha ezt nem elmulasztja, egy felugró ablakban figyelmeztetjük erre.</p>

        <img class="screen2" src="../../img/docs/aszf_message.png" alt="KÉP2">

        <p> Amennyiben a regisztráció sikeres volt, az oldal visszatér a kezdőlapra, illetve a megadott e-mail címre egy levelet kap a sikeres regisztrációról.</p>
        <p> Sikertelen regisztráció esetén az oldalon megjelenik a hiba oka. </p>

        <h2> 7. Elfelejtett jelszó</h2>
        <p> Ha elfelejtette a jelszavát, lehetősége van a bejelentkezés ablakban, az „Elfelejtette jelszavát?” szövegre kattintva elindítani az új jelszóigénylés folyamatát. Ekkor egy új oldal nyílik meg, ahol meg kell adnia a regisztrációkor használt email címét és a „Küldés” gombra kattintani.</p>

        <img class="screen2" src="../../img/docs/forget_go.png" alt="KÉP2">

        <p> Ezt követően az e-mail címére érkezni fog a jelszó megváltoztatásához szükséges link. </p>

        <p> A levélben található linken keresztül éri el a felhasználó azt a felületet, ahol módosíthatja jelszavát.</p>

        <img class="screen2" src="../../img/docs/forget.png" alt="KÉP2">

        <p> Amennyiben a jelszó módosítás sikeres volt, a „A jelszó sikeresen megváltoztatva.” üzenet jelenik meg.</p>

        <h2> 8. Menü</h2>
        <p> A képernyő jobb felső sarkában lévő „hamburger” ikonra kattintva érhetjük el a Könyvklub webshop változatos kategóriáit. </p>

        <img class="screen1" src="../../img/docs/menü.png" alt="KÉP1">

        <p> A kategóriákat használva szűrhetünk érdeklődési körünkkel megegyező könyvekre.</p>
        <p> Az „X”-re kattintva a menü becsukódik, ezzel a megoldással biztosítva a jobb helykihasználást és az átláthatóságot. A menüt csak a főoldalról érhetjük el.</p>

        <img class="screen1" src="../../img/docs/menü_close.png" alt="KÉP1">

        <h2> 9. Keresés</h2>
        <p> Webáruházunkban a felhasználók könyveinkre cím alapján tudnak keresést indítani. Lehetőségük van, nem a teljes címre keresni, hanem annak csak egy részletére is. Ebben az esetben listázásra kerül minden olyan könyv, amelynek nevében szerepel a keresett szöveg.</p>
        <p> A keresés, a mező jobb oldali részén látható nagyító ikonra kattintva indul el. Ha a kurzort az ikon fölé visszük, háttérszíne szűrkére változik. </p>

        <img class="screen1" src="../../img/docs/search.png" alt="KÉP1">

        <p> Ha keresőmezőt kitöltetlenül hagyva indítunk keresést, figyelmeztető üzenetet kapunk, ”Kérjük, töltse ki ezt a mezőt” szöveggel.</p>

        <h2> 10. Kosár</h2>
        <p> A „Bejelentkezés” és „Menü” közötti „Kosár” ikonra kattintva érjük el a kosarat.</p>

        <img class="screen1" src="../../img/docs/cart_icon.png" alt="KÉP1">

        <p> Az oldalon látható a könyvek borítóképe, címe, ára, mennyisége és fizetendő összege.
            A kosárban található könyvek a kosár ikonra kattintva törölhetők és darab számuk változtatható a „+” és „-„ gombokkal.
            A kosár teljes tartalmát a bal alsó részben található „Kosár ürítése” gombra kattintva törölheti.
            Rendelése végösszegét a kosár végén feltüntetjük.</p>
        <p> Ha folytatni szeretné rendelését, kattintson a „Tovább a pénztárhoz” gombra, ami a fizetés oldalunkra navigálja. Fizetést, és a szállítási mód kiválasztását követően a „Rendelés elküldése” gombbal véglegesítheti megrendelését. Megrendeléséről visszaigazoló levelet küldünk a megadott e-mail címére.
            Ezzel a kosár tartalma kiürül.</p>

        <img class="screen1" src="../../img/docs/cart.png" alt="KÉP1">

        <p> A kosár csak regisztrált felhasználóknak, bejelentkezés után érhető el.</p>


        <h2> 11. Kapcsolat, Információk</h2>

        <p> Az alkalmazás láblécében, ez valamennyi oldal esetében látható, a „Kapcsolat” szöveg alatt találhatóak a kapcsolattartási adatok.</p>
        <p> Az „Információ” szöveg alatt találhatóak az Általános szerződési feltételekre, az Adatvédelmi szabályzatra, a Cookie szabályzatra és a Szállítás és fizetésre mutató linkek.</p>
        <p> A „Súgó” link alatt található az alkalmazástól technológiai szempontból független weboldal, amely az alkalmazás működésével kapcsolatos információkat tartalmazza, beleértve a felhasználói kézikönyvet és útmutatót is.</p>

        <img class="screen1" src="../../img/docs/footer.png" alt="KÉP1">

